const heroImages = [
  "assets/fundadora-atelier.png",
  "assets/decoracion-neon.png",
  "assets/peonias-rosa.png"
];

let heroIndex = 0;
let currentLang = "ca";

const languageButtons = document.querySelectorAll("[data-lang-switch]");
const heroImage = document.getElementById("heroImage");
const galleryGrid = document.getElementById("galleryGrid");
const lightbox = document.getElementById("lightbox");
const lightboxImage = document.getElementById("lightboxImage");
const closeLightbox = document.getElementById("closeLightbox");

initLanguage();
startCarousel();
setupLightbox();
setupForm();

function initLanguage() {
  setLanguage("ca");

  languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const lang = button.getAttribute("data-lang-switch");
      setLanguage(lang);
    });
  });
}

function setLanguage(lang) {
  document.documentElement.lang = lang;
  currentLang = lang;
  const terms = dictionary[lang];

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (terms[key]) {
      node.textContent = terms[key];
    }
  });

  languageButtons.forEach((button) => {
    button.classList.toggle("is-active", button.getAttribute("data-lang-switch") === lang);
  });
}

function startCarousel() {
  setInterval(() => {
    heroIndex = (heroIndex + 1) % heroImages.length;
    heroImage.src = heroImages[heroIndex];
  }, 4200);
}

function setupLightbox() {
  galleryGrid.addEventListener("click", (event) => {
    const item = event.target.closest(".gallery-item");
    if (!item) return;

    const src = item.getAttribute("data-src");
    lightboxImage.src = src;
    lightbox.showModal();
  });

  closeLightbox.addEventListener("click", () => lightbox.close());

  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) {
      lightbox.close();
    }
  });
}

function setupForm() {
  const form = document.getElementById("contact-form");

  form.addEventListener("submit", (event) => {
    event.preventDefault();

    const msg = {
      ca: "Gracies. Hem rebut la teva consulta i et respondrem aviat.",
      es: "Gracias. Hemos recibido tu consulta y te responderemos pronto.",
      en: "Thank you. We received your request and will get back to you soon.",
      ja: "ありがとうございます。お問い合わせを受け付けました。近日中にご連絡いたします。"
    };

    alert(msg[currentLang]);
    form.reset();
  });
}
